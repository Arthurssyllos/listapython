# Criando a lista
numeros = [111, 7, 2, 1]
print(len(numeros)) # Imprimindo o tamanho da lista (qunatos itens tem dentro dela).
print(numeros) # Imprimindo a lista

# Adicionando um valor na lista

numeros.append(4)

print(len(numeros)) # Imprimindo o tamanho da lista (qunatos itens tem dentro dela).
print(numeros) # Imprimindo a lista

# Inserindo um valor na lista em uma posição específica

numeros.insert(0, 222) # o .insert serve para inserir um valor, no caso posição e em seguida o valor. No caso do exemplo, posição 0 e valor 222
print(len(numeros)) # Imprimindo o tamanho da lista (qunatos itens tem dentro dela).
print(numeros) # Imprimindo a lista